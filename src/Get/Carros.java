package Get;

public class Carros{

	private int Codcar;
	private String NameCar;
	private float VlCar;
	private float LocCar;
	private String ModCar;
	private String MarCar;
	
	public int getCodcar() {
		return Codcar;
	}
	public void setCodcar(int codcar) {
		Codcar = codcar;
	}
	public String getNameCar() {
		return NameCar;
	}
	public void setNameCar(String nameCar) {
		NameCar = nameCar;
	}
	public float getVlCar() {
		return VlCar;
	}
	public void setVlCar(float vlCar) {
		VlCar = vlCar;
	}
	public float getLocCar() {
		return LocCar;
	}
	public void setLocCar(float locCar) {
		LocCar = locCar;
	}
	public String getModCar() {
		return ModCar;
	}
	public void setModCar(String modCar) {
		ModCar = modCar;
	}
	public String getMarCar() {
		return MarCar;
	}
	public void setMarCar(String marCar) {
		MarCar = marCar;
	}
	
	
		
}
